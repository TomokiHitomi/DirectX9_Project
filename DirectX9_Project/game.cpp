//=============================================================================
// 更新処理
//=============================================================================
void GameScene::Update(void)
{
	UpdateInput();		// 入力

	UpdatePlayer();		// プレイヤー

	UpdateCamera();		// カメラ

#ifdef _DEBUG
	UpdateDebugProc();
	PrintDebugProc("GameScene[Update]\n");
#endif
}

//=============================================================================
// 描画処理
//=============================================================================
void GameScene::Draw(void)
{
	SetCamera();		// カメラ

	DrawPlayer();		// プレイヤー

#ifdef _DEBUG
	PrintDebugProc("GameScene[Draw]\n");
	DrawDebugProc();
#endif
}

//=============================================================================
// コンストラクタ処理
//=============================================================================
GameScene::GameScene(void)
{
	InitPlayer(0);
}

//=============================================================================
// デストラクタ処理
//=============================================================================
GameScene::~GameScene(void)
{
	UninitPlayer();
}